<?php
/*
* Classe Somar
* 
* Está classe vai pegar um valor X e somar com um valor Y
*
* @packege ControleDeEstoque
* $param $x float é o primeiro valor a ser somado
* $param $y float é o segundo valor a ser somado
* $return float
*/

class Somar
{
    public function somar($x, $y)
    {
        return $x + $y;
    }
}